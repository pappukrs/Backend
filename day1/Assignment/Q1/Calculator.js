

const add=(a,b)=>{
    return a+b;
}

const sub=(a,b)=>{
    return a-b;
}

const mul=(a,b)=>{
    return a*b;
}

const divid=(a,b)=>{
    return a/b;
}
const sin=(a)=>{
    return Math.sin(a)
}
const cos=(a)=>{
    return Math.cos(a)
}
const tan=(a)=>{
    return Math.tan(a)
}



module.exports={add,sub,mul,divid,sin,cos,tan};